    private static let searchScript: String = """
    (function() {
      var indexEl = document.getElementById('file-index');
      var input = document.getElementById('q');
      var resultsEl = document.getElementById('results');
      var statusEl = document.getElementById('status');
      if (!indexEl || !input || !resultsEl) return;

      var INDEX = {};
      try { INDEX = JSON.parse(indexEl.textContent || '{}'); } catch (e) { INDEX = {}; }

      var FLAT = [];
      for (var folder in INDEX) {
        if (!Object.prototype.hasOwnProperty.call(INDEX, folder)) continue;
        var files = INDEX[folder];
        for (var i = 0; i < files.length; i++) {
          var name = files[i];
          FLAT.push({ file: name, fileLower: name.toLowerCase(), folder: folder });
        }
      }
      statusEl.textContent = 'Ready. ' + FLAT.length + ' file name(s) indexed.';

      var debounceTimer;
      input.addEventListener('input', function() {
        clearTimeout(debounceTimer);
        debounceTimer = setTimeout(runSearch, 80);
      });

      function runSearch() {
        var raw = (input.value || '').trim();
        if (raw.length < 2) {
          resultsEl.innerHTML = '';
          statusEl.textContent = FLAT.length + ' file name(s) indexed.';
          return;
        }
        var q = raw.toLowerCase();
        var t0 = performance.now ? performance.now() : Date.now();
        var hits = [];
        var limit = 200;
        for (var i = 0; i < FLAT.length; i++) {
          var entry = FLAT[i];
          var s = score(q, entry.fileLower);
          if (s > 0) {
            hits.push({ entry: entry, score: s });
          }
        }
        hits.sort(function(a, b) { return b.score - a.score; });
        var capped = hits.length > limit;
        hits = hits.slice(0, limit);
        var t1 = performance.now ? performance.now() : Date.now();
        statusEl.textContent = hits.length + (capped ? '+' : '') + ' match(es) in ' + (t1 - t0).toFixed(0) + ' ms';
        renderHits(hits, raw);
      }

      function score(q, t) {
        if (t === q) return 1000;
        if (t.indexOf(q) === 0) return 900 - Math.min(99, t.length - q.length);
        var sep = '/.-_';
        for (var i = 0; i < t.length - q.length; i++) {
          if (sep.indexOf(t.charAt(i)) >= 0 && t.substr(i + 1, q.length) === q) {
            return 800 - Math.min(99, i);
          }
        }
        var idx = t.indexOf(q);
        if (idx >= 0) return 700 - Math.min(99, idx);
        var qi = 0, lastMatch = -1, gaps = 0;
        for (var ti = 0; ti < t.length && qi < q.length; ti++) {
          if (t.charAt(ti) === q.charAt(qi)) {
            if (lastMatch >= 0) gaps += ti - lastMatch - 1;
            lastMatch = ti;
            qi++;
          }
        }
        if (qi === q.length) {
          return Math.max(100, 500 - gaps - Math.min(200, t.length - q.length));
        }
        return 0;
      }

      function renderHits(hits, q) {
        if (hits.length === 0) {
          resultsEl.innerHTML = '<p class="muted">No matches.</p>';
          return;
        }
        var rows = '';
        for (var i = 0; i < hits.length; i++) {
          var e = hits[i].entry;
          rows += '<tr><td class="file-cell">' + highlight(e.file, q) +
                  '</td><td class="folder-cell">' + esc(e.folder) +
                  '</td><td class="score">' + hits[i].score + '</td></tr>';
        }
        resultsEl.innerHTML =
          '<table><thead><tr><th>File</th><th>Folder</th><th class="score">Score</th></tr></thead><tbody>' +
          rows + '</tbody></table>';
      }

      function highlight(text, q) {
        var t = text.toLowerCase();
        var ql = q.toLowerCase();
        var idx = t.indexOf(ql);
        if (idx < 0) return esc(text);
        return esc(text.slice(0, idx)) + '<mark>' + esc(text.slice(idx, idx + q.length)) + '</mark>' + esc(text.slice(idx + q.length));
      }

      function esc(s) {
        return String(s).replace(/[&<>"']/g, function(c) {
          return { '&': '&amp;', '<': '&lt;', '>': '&gt;', '"': '&quot;', "'": '&#39;' }[c];
        });
      }
    })();
    """
