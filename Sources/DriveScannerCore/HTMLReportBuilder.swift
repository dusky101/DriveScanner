    private static let reportScript: String = """
    (function() {
      // ---- Sortable tables ----
      document.querySelectorAll('table.sortable').forEach(function(table) {
        var headers = table.querySelectorAll('thead th');
        headers.forEach(function(th, idx) {
          th.addEventListener('click', function() {
            sortColumn(table, idx, th);
          });
        });
      });

      function sortColumn(table, idx, th) {
        var tbody = table.querySelector('tbody');
        var rows = Array.prototype.slice.call(tbody.querySelectorAll('tr'));
        var isNum = th.classList.contains('num');
        var current = th.getAttribute('data-dir');
        var ascending = current !== 'asc';
        table.querySelectorAll('thead th').forEach(function(h) { h.removeAttribute('data-dir'); });
        th.setAttribute('data-dir', ascending ? 'asc' : 'desc');
        rows.sort(function(a, b) {
          var av = a.cells[idx] ? a.cells[idx].textContent.trim() : '';
          var bv = b.cells[idx] ? b.cells[idx].textContent.trim() : '';
          var cmp;
          if (isNum) {
            cmp = parseBytes(av) - parseBytes(bv);
          } else {
            // Date column ("16 May 2026") fallback to string compare — good enough for same-locale exports
            cmp = av.localeCompare(bv, undefined, { numeric: true, sensitivity: 'base' });
          }
          return ascending ? cmp : -cmp;
        });
        rows.forEach(function(r) { tbody.appendChild(r); });
      }

      function parseBytes(text) {
