import Foundation

/// Builds a single self-contained, user-branded HTML migration report.
public enum HTMLReportBuilder {
    public static func buildReport(
        userContext: UserContext,
        selectedItems: [CandidateItem],
        excludedItems: [CandidateItem],
        rollup: FolderRollupResult,
        mediaMeasurements: [MediaFolderMeasurement],
        homebrew: HomebrewInfo?,
        fileIndex: [String: [String]] = [:],
        generatedAt: Date = Date()
    ) -> String {
        let totalSelectedBytes = selectedItems.reduce(Int64(0)) { $0 + $1.sizeBytes }
        let mediaTotalBytes = mediaMeasurements.reduce(Int64(0)) { $0 + (($1.exists ? $1.totalBytes : 0)) }
        let devConfigCount = selectedItems.filter { $0.category == .devConfig }.count
        let projectCount = selectedItems.filter { $0.category == .codeProject }.count

        let dateFormatter = DateFormatter()
        dateFormatter.dateStyle = .long
        dateFormatter.timeStyle = .short
        let generatedLong = dateFormatter.string(from: generatedAt)

        let hero = renderHero(
            userContext: userContext,
            selectedCount: selectedItems.count,
            totalSelectedBytes: totalSelectedBytes,
            mediaTotalBytes: mediaTotalBytes,
            devConfigCount: devConfigCount,
            projectCount: projectCount,
            homebrew: homebrew,
            generatedLong: generatedLong
        )
        let searchSection = renderSearchSection(fileIndex: fileIndex)
        let included = renderItems(title: "Folders to migrate", items: selectedItems, isExcluded: false, emptyMessage: "No items selected.")
        let breakdown = renderBreakdown(rollup)
        let brewSection = renderHomebrew(homebrew)
        let media = renderMedia(mediaMeasurements)
        let excluded = excludedItems.isEmpty
            ? ""
            : renderItems(title: "Excluded (not migrating)", items: excludedItems, isExcluded: true, emptyMessage: "")

        let fileIndexJson = encodeJson(fileIndex)
        let title = "Migration report — \(userContext.fullName)"

        return """
        <!DOCTYPE html>
        <html lang="en">
        <head>
        <meta charset="utf-8"/>
        <meta name="viewport" content="width=device-width, initial-scale=1"/>
        <title>\(escape(title))</title>
        <style>
        \(stylesheet)
        </style>
        </head>
        <body>
        <div class="wrap">
        \(hero)
        \(searchSection)
        \(included)
        \(breakdown)
        \(brewSection)
        \(media)
        \(excluded)
        <footer>Generated by DriveScanner on \(escape(generatedLong))</footer>
        </div>
        <script id="file-index" type="application/json">\(fileIndexJson)</script>
        <script>
        \(reportScript)
        </script>
        </body>
        </html>
        """
    }

    private static func encodeJson(_ value: [String: [String]]) -> String {
        guard let data = try? JSONSerialization.data(withJSONObject: value, options: []) else { return "{}" }
        let raw = String(data: data, encoding: .utf8) ?? "{}"
        // Prevent `</script>` injection inside the embedded JSON.
        return raw
            .replacingOccurrences(of: "</", with: "<\\/")
            .replacingOccurrences(of: "\u{2028}", with: "\\u2028")
            .replacingOccurrences(of: "\u{2029}", with: "\\u2029")
    }

    private static func renderSearchSection(fileIndex: [String: [String]]) -> String {
        if fileIndex.isEmpty {
            return ""
        }
        let totalFiles = fileIndex.values.reduce(0) { $0 + $1.count }
        return """
        <section id="search-section">
          <h2>Search files inside selected folders</h2>
          <p class="muted" style="margin:0 0 0.65rem;">Indexed \(totalFiles) file name(s) across \(fileIndex.count) folder(s). Type at least 2 characters.</p>
          <input id="globalSearch" type="search" placeholder="e.g. Package.swift, .env, config.json" autocomplete="off"/>
          <div id="searchResults"></div>
        </section>
        """
    }

    public static func escape(_ s: String) -> String {
        var out = ""
        out.reserveCapacity(s.count)
        for ch in s.unicodeScalars {
            switch ch {
            case "&": out.append("&amp;")
            case "<": out.append("&lt;")
            case ">": out.append("&gt;")
            case "\"": out.append("&quot;")
            case "'": out.append("&#39;")
            default:
                if ch.value < 32 || ch.value == 0x7F {
                    out.append("&#\(ch.value);")
                } else {
                    out.unicodeScalars.append(ch)
                }
            }
        }
        return out
    }

    // MARK: - Sections

    private static func renderHero(
        userContext: UserContext,
        selectedCount: Int,
        totalSelectedBytes: Int64,
        mediaTotalBytes: Int64,
        devConfigCount: Int,
        projectCount: Int,
        homebrew: HomebrewInfo?,
        generatedLong: String
    ) -> String {
        let initials = escape(userContext.initials)
        let fullName = escape(userContext.fullName.isEmpty ? userContext.shortName : userContext.fullName)
        let subline = escape("\(userContext.shortName)@\(userContext.hostName) · \(userContext.osVersion) · \(generatedLong)")
        let sizeStr = escape(formatBytes(totalSelectedBytes))
        let mediaStr = escape(formatBytes(mediaTotalBytes))
        let brewKpi: String
        if let h = homebrew, !h.isEmpty {
            brewKpi = """
            <div class="kpi"><span class="kpi-label">Homebrew</span><span class="kpi-value">\(h.formulaCount + h.caskCount)</span><span class="kpi-sub">formulae + casks</span></div>
            """
        } else {
            brewKpi = """
            <div class="kpi"><span class="kpi-label">Homebrew</span><span class="kpi-value">—</span><span class="kpi-sub">not detected</span></div>
            """
        }

        return """
        <header class="hero">
          <div class="hero-top">
            <div class="avatar">\(initials)</div>
            <div>
              <h1>\(fullName)</h1>
              <p class="sub">\(subline)</p>
            </div>
          </div>
          <div class="kpis">
            <div class="kpi"><span class="kpi-label">Selected to migrate</span><span class="kpi-value">\(selectedCount)</span><span class="kpi-sub">items</span></div>
            <div class="kpi"><span class="kpi-label">Total size</span><span class="kpi-value">\(sizeStr)</span><span class="kpi-sub">allocated on disk</span></div>
            <div class="kpi"><span class="kpi-label">Code projects</span><span class="kpi-value">\(projectCount)</span><span class="kpi-sub">detected</span></div>
            <div class="kpi"><span class="kpi-label">Dev configs</span><span class="kpi-value">\(devConfigCount)</span><span class="kpi-sub">dotfiles</span></div>
            \(brewKpi)
            <div class="kpi"><span class="kpi-label">Media folders</span><span class="kpi-value">\(mediaStr)</span><span class="kpi-sub">pictures + music + movies</span></div>
          </div>
        </header>
        """
    }

    private static func renderItems(title: String, items: [CandidateItem], isExcluded: Bool, emptyMessage: String) -> String {
        let sectionClass = isExcluded ? "section-excluded" : ""
        if items.isEmpty {
            if emptyMessage.isEmpty { return "" }
            return """
            <section class="\(sectionClass)">
              <h2>\(escape(title))</h2>
              <p class="empty">\(escape(emptyMessage))</p>
            </section>
            """
        }

        let modFormatter = DateFormatter()
        modFormatter.dateStyle = .medium
        modFormatter.timeStyle = .none

        let rows = items
            .sorted { $0.sizeBytes > $1.sizeBytes }
            .map { item -> String in
                let catTag = """
                <span class="tag \(item.category.tagClass)">\(escape(item.category.displayLabel.uppercased()))</span>
                """
                let stackTag: String = item.stack.map {
                    "<span class=\"tag tag-stack\">\(escape($0.displayLabel.uppercased()))</span>"
                } ?? ""
                let symTag = item.isSymlink ? "<span class=\"tag tag-warn\">SYMLINK</span>" : ""
                let fileTag = (!item.isDirectory && !item.isSymlink) ? "<span class=\"tag tag-file\">FILE</span>" : ""
                let modStr = item.modificationDate.map { modFormatter.string(from: $0) } ?? "—"
                return """
                <tr>
                  <td>
                    <div class="row-name">\(escape(item.name))</div>
                    <div class="row-tags">\(catTag)\(stackTag)\(symTag)\(fileTag)</div>
                  </td>
                  <td class="path">\(escape(displayPath(item.url)))</td>
                  <td class="num">\(escape(formatBytes(item.sizeBytes)))</td>
                  <td>\(escape(modStr))</td>
                </tr>
                """
            }
            .joined(separator: "\n")

        return """
        <section class="\(sectionClass)">
          <h2>\(escape(title)) <span class="section-count">(\(items.count))</span></h2>
          <table class="sortable">
            <thead><tr><th>Name</th><th>Path</th><th class="num">Size</th><th>Modified</th></tr></thead>
            <tbody>
        \(rows)
            </tbody>
          </table>
        </section>
        """
    }

    private static func renderBreakdown(_ rollup: FolderRollupResult) -> String {
        let nonEmpty = rollup.perRoot.filter { !$0.depth1.isEmpty || !$0.looseFiles.isEmpty }
        guard !nonEmpty.isEmpty else { return "" }

        let articles = nonEmpty.map { per -> String in
            let name = escape(per.rootDisplayName)
            var blocks: [String] = ["<h3>\(name)</h3>"]
            if !per.depth1.isEmpty {
                blocks.append(renderBucketTable(title: "Top sub-folders", buckets: per.depth1))
            }
            if !per.looseFiles.isEmpty {
                blocks.append(renderBucketTable(title: "Loose files at root", buckets: per.looseFiles))
            }
            return blocks.joined(separator: "\n")
        }.joined(separator: "\n")

        return """
        <section>
          <h2>Inside each selected folder</h2>
        \(articles)
        </section>
        """
    }

    private static func renderBucketTable(title: String, buckets: [FolderRollupBucket]) -> String {
        let rows = buckets.map { bucket -> String in
            return """
            <tr>
              <td>\(escape(bucket.label))</td>
              <td class="num">\(bucket.fileCount)</td>
              <td class="num">\(escape(formatBytes(bucket.totalBytes)))</td>
            </tr>
            """
        }.joined(separator: "\n")

        return """
        <p class="bucket-title">\(escape(title))</p>
        <table>
          <thead><tr><th>Sub-folder</th><th class="num">Files</th><th class="num">Size</th></tr></thead>
          <tbody>
        \(rows)
          </tbody>
        </table>
        """
    }

    private static func renderHomebrew(_ info: HomebrewInfo?) -> String {
        guard let info, !info.isEmpty else { return "" }
        let restoreCmd = HomebrewService.restoreCommand()
        let escapedBrewfile = escape(info.brewfile)

        return """
        <section>
          <h2>Homebrew</h2>
          <p class="muted" style="margin:0 0 0.75rem;">Detected at <code>\(escape(info.brewPath))</code></p>
          <div class="kpis kpis-inline">
            <div class="kpi"><span class="kpi-label">Formulae</span><span class="kpi-value">\(info.formulaCount)</span></div>
            <div class="kpi"><span class="kpi-label">Casks</span><span class="kpi-value">\(info.caskCount)</span></div>
            <div class="kpi"><span class="kpi-label">Taps</span><span class="kpi-value">\(info.tapCount)</span></div>
            <div class="kpi"><span class="kpi-label">Mac App Store</span><span class="kpi-value">\(info.masCount)</span></div>
          </div>
          <h3>Restore on the new Mac</h3>
          <ol class="steps">
            <li>Install Homebrew: <code>/bin/bash -c "$(curl -fsSL https://raw.githubusercontent.com/Homebrew/install/HEAD/install.sh)"</code></li>
            <li>Copy the <code>Brewfile</code> from your migration bundle to the new Mac.</li>
            <li>Run <code>\(escape(restoreCmd))</code> from the folder containing the Brewfile.</li>
          </ol>
          <details class="brewfile">
            <summary>View Brewfile (\(info.totalItems) entries)</summary>
            <pre>\(escapedBrewfile)</pre>
          </details>
        </section>
        """
    }

    private static func renderMedia(_ measurements: [MediaFolderMeasurement]) -> String {
        guard !measurements.isEmpty else { return "" }
        let rows = measurements.map { m -> String in
            let sizeCell = m.exists ? escape(formatBytes(m.totalBytes)) : "<span class=\"muted\">not found</span>"
            return """
            <tr>
              <td>\(escape(m.folder.displayLabel))</td>
              <td class="path">~/\(escape(m.folder.directoryName))</td>
              <td class="num">\(sizeCell)</td>
            </tr>
            """
        }.joined(separator: "\n")

        return """
        <section>
          <h2>Standard media folders</h2>
          <table>
            <thead><tr><th>Folder</th><th>Path</th><th class="num">Size</th></tr></thead>
            <tbody>
        \(rows)
            </tbody>
          </table>
        </section>
        """
    }

    // MARK: - Helpers

    private static func formatBytes(_ bytes: Int64) -> String {
        ByteCountFormatter.string(fromByteCount: bytes, countStyle: .file)
    }

    private static func displayPath(_ url: URL) -> String {
        let home = NSHomeDirectory()
        let p = url.path
        if p.hasPrefix(home) {
            return "~" + p.dropFirst(home.count)
        }
        return p
    }

