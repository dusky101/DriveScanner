import Foundation

/// Builds a standalone, dependency-free `file-search.html` page with a ranked search over
/// the file-name index produced by `HomeScanner.measureDirectory`.
///
/// Ranking model (descending):
///   1. exact match
///   2. starts-with
///   3. word-boundary match (after `/`, `.`, `-`, `_`)
///   4. substring (earlier position = higher score)
///   5. subsequence / fuzzy match (denser match = higher score)
public enum FileSearchHTMLBuilder {
    public static func buildSearchPage(
        userContext: UserContext,
        fileIndex: [String: [String]],
        inventoryLinkHref: String = "inventory.html",
        generatedAt: Date = Date()
    ) -> String {
        let totalFiles = fileIndex.values.reduce(0) { $0 + $1.count }
        let folderCount = fileIndex.count

        let dateFormatter = DateFormatter()
        dateFormatter.dateStyle = .long
        dateFormatter.timeStyle = .short
        let generatedLong = dateFormatter.string(from: generatedAt)

        let title = "File search — \(userContext.fullName)"
        let fullName = HTMLReportBuilder.escape(userContext.fullName.isEmpty ? userContext.shortName : userContext.fullName)
        let subline = HTMLReportBuilder.escape("\(userContext.shortName)@\(userContext.hostName) · \(userContext.osVersion) · \(generatedLong)")

        let indexJson = encodeJson(fileIndex)

        return """
        <!DOCTYPE html>
        <html lang="en">
        <head>
        <meta charset="utf-8"/>
        <meta name="viewport" content="width=device-width, initial-scale=1"/>
        <title>\(HTMLReportBuilder.escape(title))</title>
        <style>
        \(stylesheet)
        </style>
        </head>
        <body>
        <div class="wrap">
          <header class="hero">
            <h1>File search</h1>
            <p class="sub">\(fullName) — \(subline)</p>
            <p class="back"><a href="\(HTMLReportBuilder.escape(inventoryLinkHref))">← Back to migration report</a></p>
          </header>

          <section>
            <input id="q" type="search" autocomplete="off" autofocus
              placeholder="Type at least 2 characters — try: Package.swift, .env, node_modules"/>
            <p class="hint">Indexed <strong>\(totalFiles)</strong> file name(s) across <strong>\(folderCount)</strong> selected folder(s).</p>
            <p id="status" class="muted"></p>
            <div id="results"></div>
          </section>

          <footer>Generated by DriveScanner on \(HTMLReportBuilder.escape(generatedLong)) · Search runs entirely in your browser; no data is sent anywhere.</footer>
        </div>
        <script id="file-index" type="application/json">\(indexJson)</script>
        <script>
        \(searchScript)
        </script>
        </body>
        </html>
        """
    }

    // MARK: - JSON encoding

    private static func encodeJson(_ value: [String: [String]]) -> String {
        guard let data = try? JSONSerialization.data(withJSONObject: value, options: []) else { return "{}" }
        let raw = String(data: data, encoding: .utf8) ?? "{}"
        return raw
            .replacingOccurrences(of: "</", with: "<\\/")
            .replacingOccurrences(of: "\u{2028}", with: "\\u2028")
            .replacingOccurrences(of: "\u{2029}", with: "\\u2029")
    }

